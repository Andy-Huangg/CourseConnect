interface EmojiPickerProps {
  onEmojiSelect: (emoji: string) => void;
  anchorEl?: HTMLElement | null;
  open: boolean;
  onClose: () => void;
}

export default function EmojiPicker({
  onEmojiSelect,
  open,
  onClose,
}: EmojiPickerProps) {
  const theme = useTheme();
  const [currentTab, setCurrentTab] = useState(0);
  const [searchTerm, setSearchTerm] = useState("");
  const [recentEmojis, setRecentEmojis] = useState<string[]>([]);

  // Load recent emojis from localStorage
  useEffect(() => {
    const saved = localStorage.getItem("recentEmojis");
    if (saved) {
      try {
        setRecentEmojis(JSON.parse(saved));
      } catch {
        setRecentEmojis([]);
      }
    }
  }, []);

  // Update recent emojis in localStorage
  const updateRecentEmojis = (emoji: string) => {
    const updated = [emoji, ...recentEmojis.filter((e) => e !== emoji)].slice(
      0,
      32
    );
    setRecentEmojis(updated);
    localStorage.setItem("recentEmojis", JSON.stringify(updated));
  };

  const handleEmojiClick = (emoji: string) => {
    updateRecentEmojis(emoji);
    onEmojiSelect(emoji);
    onClose();
  };

  const categories = [
    { ...emojiCategories.recent, emojis: recentEmojis },
    emojiCategories.smileys,
    emojiCategories.animals,
    emojiCategories.food,
    emojiCategories.activities,
    emojiCategories.objects,
    emojiCategories.symbols,
  ];

  const currentCategory = categories[currentTab];
  const filteredEmojis = searchTerm
    ? categories
        .flatMap((cat) => cat.emojis)
        .filter((emoji) => emoji.includes(searchTerm))
    : currentCategory.emojis;

  if (!open) return null;

  return (
    <ClickAwayListener onClickAway={onClose}>
      <EmojiContainer>
        {/* Search */}
        <Box sx={{ p: 1, borderBottom: `1px solid ${theme.palette.divider}` }}>
          <TextField
            size="small"
            fullWidth
            placeholder="Search emojis..."
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
            InputProps={{
              startAdornment: <SearchIcon sx={{ mr: 1, color: "text.secondary" }} />,
            }}
            sx={{
              "& .MuiOutlinedInput-root": {
                borderRadius: 2,
              },
            }}
          />
        </Box>

        {/* Category Tabs */}
        {!searchTerm && (
          <Tabs
            value={currentTab}
            onChange={(_, newValue) => setCurrentTab(newValue)}
            variant="scrollable"
            scrollButtons="auto"
            sx={{
              minHeight: "auto",
              "& .MuiTab-root": {
                minHeight: "auto",
                py: 1,
                px: 2,
                fontSize: "1rem",
              },
            }}
          >
            <Tab label="ðŸ•" title="Recent" />
            <Tab label="ðŸ˜Š" title="Smileys & People" />
            <Tab label="ðŸ¶" title="Animals & Nature" />
            <Tab label="ðŸŽ" title="Food & Drink" />
            <Tab label="âš½" title="Activities" />
            <Tab label="ðŸ’¡" title="Objects" />
            <Tab label="â¤ï¸" title="Symbols" />
          </Tabs>
        )}

        {/* Emoji Grid */}
        <EmojiGrid>
          {filteredEmojis.length === 0 ? (
            <Box
              sx={{
                gridColumn: "1 / -1",
                textAlign: "center",
                py: 4,
                color: "text.secondary",
              }}
            >
              <Typography variant="body2">
                {searchTerm ? "No emojis found" : "No recent emojis"}
              </Typography>
            </Box>
          ) : (
            filteredEmojis.map((emoji, index) => (
              <Tooltip key={`${emoji}-${index}`} title={emoji} arrow>
                <EmojiButton onClick={() => handleEmojiClick(emoji)}>
                  {emoji}
                </EmojiButton>
              </Tooltip>
            ))
          )}
        </EmojiGrid>
      </EmojiContainer>
    </ClickAwayListener>
  );
}

// Export the trigger button component as well
export function EmojiPickerTrigger({
  onClick,
  disabled = false,
}: {
  onClick: () => void;
  disabled?: boolean;
}) {
  return (
    <Tooltip title="Add emoji">
      <IconButton
        size="small"
        onClick={onClick}
        disabled={disabled}
        sx={{
          mb: 0.25,
          display: { xs: "none", sm: "inline-flex" },
          "&:hover": {
            backgroundColor: "action.hover",
          },
        }}
      >
        <EmojiEmotionsIcon />
      </IconButton>
    </Tooltip>
  );
}
