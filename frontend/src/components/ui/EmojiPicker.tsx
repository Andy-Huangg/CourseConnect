interface EmojiPickerProps {
  onEmojiSelect: (emoji: string) => void;
  anchorEl?: HTMLElement | null;
  open: boolean;
  onClose: () => void;
}

export default function EmojiPicker({
  onEmojiSelect,
  open,
  onClose,
}: EmojiPickerProps) {
  const [currentTab, setCurrentTab] = useState(0);

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
    onClose();
  };

  const categories = [
    emojiCategories.smileys,
    emojiCategories.animals,
    emojiCategories.food,
    emojiCategories.activities,
    emojiCategories.objects,
    emojiCategories.symbols,
  ];

  const currentCategory = categories[currentTab];

  if (!open) return null;

  return (
    <ClickAwayListener
      onClickAway={(event) => {
        // Don't close if clicking on the emoji trigger button
        const target = event.target as HTMLElement;
        if (target.closest("[data-emoji-trigger]")) {
          return;
        }
        onClose();
      }}
    >
      <EmojiContainer>
        {/* Category Tabs */}
        <Tabs
          value={currentTab}
          onChange={(_, newValue) => setCurrentTab(newValue)}
          variant="scrollable"
          scrollButtons="auto"
          sx={{
            minHeight: "auto",
            "& .MuiTab-root": {
              minHeight: "auto",
              py: 1,
              px: 2,
              fontSize: "1rem",
            },
          }}
        >
          <Tab label="😊" title="Smileys & People" />
          <Tab label="🐶" title="Animals & Nature" />
          <Tab label="🍎" title="Food & Drink" />
          <Tab label="⚽" title="Activities" />
          <Tab label="💡" title="Objects" />
          <Tab label="❤️" title="Symbols" />
        </Tabs>

        {/* Emoji Grid */}
        <EmojiGrid>
          {currentCategory.emojis.length === 0 ? (
            <Box
              sx={{
                gridColumn: "1 / -1",
                textAlign: "center",
                py: 4,
                color: "text.secondary",
              }}
            >
              <Typography variant="body2">No emojis available</Typography>
            </Box>
          ) : (
            currentCategory.emojis.map((emoji: string, index: number) => (
              <Tooltip key={`${emoji}-${index}`} title={emoji} arrow>
                <EmojiButton onClick={() => handleEmojiClick(emoji)}>
                  {emoji}
                </EmojiButton>
              </Tooltip>
            ))
          )}
        </EmojiGrid>
      </EmojiContainer>
    </ClickAwayListener>
  );
}

// Export the trigger button component as well
export function EmojiPickerTrigger({
  onClick,
  disabled = false,
}: {
  onClick: () => void;
  disabled?: boolean;
}) {
  return (
    <Tooltip title="Add emoji">
      <IconButton
        size="small"
        onClick={onClick}
        disabled={disabled}
        data-emoji-trigger="true"
        sx={{
          minWidth: { xs: "32px", md: "36px" },
          minHeight: { xs: "32px", md: "36px" },
          padding: { xs: "6px", md: "8px" },
          display: { xs: "none", sm: "inline-flex" },
          "&:hover": {
            backgroundColor: "action.hover",
          },
        }}
      >
        <EmojiEmotionsIcon sx={{ fontSize: { xs: "18px", md: "20px" } }} />
      </IconButton>
    </Tooltip>
  );
}
